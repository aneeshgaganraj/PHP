<?php
  // Declaring variables
  $greeting = "Hello, World!";
  $number = 10;
  $decimal = 3.14;
  $isTrue = true;

  // Outputting variables
  echo $greeting;  // Outputs: Hello, World!
  echo "<br>";     // Line break in HTML
  echo "Number: " . $number;  // Outputs: Number: 10
  echo "<br>";
  echo "Decimal: " . $decimal;  // Outputs: Decimal: 3.14
  echo "<br>";
  echo "Boolean: " . $isTrue;  // Outputs: Boolean: 1 (true is represented as 1 in PHP)
?>
